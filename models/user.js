const mongoose = require("mongoose");

//===============================================================
// Schema for the model of the database
// Model is created using the schema where we define types
//===============================================================
const User = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
    default: Date.now,
  },
});

module.exports = mongoose.model("user", User);
